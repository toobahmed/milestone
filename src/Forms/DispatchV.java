    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        try {
            Statement s = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = s.executeQuery("SELECT * FROM dispatched");
            rs.moveToInsertRow();
            rs.updateInt("v_id", frs.getInt("id"));
            rs.updateInt("c_id", crs.getInt("id"));
            rs.updateLong("dispatch_time", System.currentTimeMillis());
            rs.insertRow();
            rs.close();
            s.close();
            frs.updateInt("available", 0);
            frs.updateRow();

            JOptionPane.showMessageDialog(this, "Vehicle dispatched");
            this.setVisible(false);

        } catch (SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

// TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

