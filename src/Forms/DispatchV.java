    void doConnect() {
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/milestone", "root", "");
            stmt1 = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            stmt2 = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);

            frs = stmt1.executeQuery("SELECT * FROM FLEET WHERE available>0");
            frs.next();
            displayF();
            crs = stmt2.executeQuery("SELECT * FROM CUSTOMER");
            crs.next();
            displayC();



        } catch (ClassNotFoundException | SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

    void displayF() {
        try {

            jLabel11.setText(frs.getString("reg_no"));
            jLabel14.setText(frs.getString("make"));
            jLabel15.setText(frs.getString("model"));
            jLabel16.setText(frs.getString("f_year"));
            jLabel17.setText(frs.getString("color") + "/" + frs.getString("type"));
            jLabel18.setText("Seats " + Integer.toString(frs.getInt("seats")));
            jLabel12.setText(Double.toString(frs.getDouble("deposit")));
            jLabel13.setText(Double.toString(frs.getDouble("rate")));

            jPanel2.removeAll();
            Image img = ImageIO.read(new File(frs.getString("url")));
            Image i = img.getScaledInstance(jPanel2.getWidth(), jPanel2.getHeight(), Image.SCALE_SMOOTH);
            JLabel label = new JLabel("", new ImageIcon(i), JLabel.CENTER);
            label.setVisible(true);
            jPanel2.setLayout(new BorderLayout());
            jPanel2.add(label, BorderLayout.CENTER);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

