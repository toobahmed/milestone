    private void jMenuItem3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem3ActionPerformed
        try {
            ResultSet rs = stmt.executeQuery("SELECT COUNT(*) FROM fleet WHERE available>0");
            rs.next();
            int f = rs.getInt(1);
            rs = stmt.executeQuery("SELECT COUNT(*) FROM customer");
            rs.next();
            int c = rs.getInt(1);
            if (f == 0) {
                JOptionPane.showMessageDialog(this, "No Vehicles available");
            } else if (c == 0) {
                JOptionPane.showMessageDialog(this, "No Customers");
            } else {

                DispatchV dv = new DispatchV();
                dv.setVisible(true);
                dv.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
            }
            rs.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }// TODO add your handling code here:
    }//GEN-LAST:event_jMenuItem3ActionPerformed

