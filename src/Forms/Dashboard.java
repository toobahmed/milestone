    public void doConnect() {
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/milestone", "root", "");
            stmt = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = stmt.executeQuery("SELECT count(*) FROM customer");
            rs.next();
            jLabel21.setText(Integer.toString(rs.getInt(1)));

            rs = stmt.executeQuery("SELECT count(*) FROM fleet");
            rs.next();
            jLabel17.setText(Integer.toString(rs.getInt(1)));

            rs = stmt.executeQuery("SELECT count(*) FROM fleet WHERE available=0");
            rs.next();
            jLabel18.setText(Integer.toString(rs.getInt(1)));

            rs = stmt.executeQuery("SELECT count(*) FROM fleet WHERE available>0");
            rs.next();
            jLabel16.setText(Integer.toString(rs.getInt(1)));

            rs = stmt.executeQuery("SELECT sum(total_amt) AS tamt,sum(distance_driven) AS dd,count(*) AS tr FROM sales");
            rs.next();
            jLabel15.setText(Integer.toString(rs.getInt("tr")));
            jLabel19.setText(Integer.toString(rs.getInt("tamt")));
            jLabel22.setText(Integer.toString(rs.getInt("dd")));
            rs.close();
        } catch (ClassNotFoundException | SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

